#include "3-calc.h"
/**
 * op_add - returns sum of a + b.
 * @a: first integer.
 * @b: second integer.
 * Return: sum.
 */

int op_add(int a, int b)
{

	return (a + b);

}


/**
 * op_sub - returns difference of a & b.
 * @a: first integer.
 * @b: second integer.
 * Return: diference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - returns product of a & b.
* @a: first integer.
* @b: second integer.
* Return: product.
*/
int op_mul(int a, int b)
{

	return (a * b);
}
/**
* op_div - returns sum of a & b.
* @a: first integer.
* @b: second integer.
* Return: result of divsision.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - returns remainder of a & b.
* @a: first integer.
* @b: second integer.
* Return: remainder.
*/

int op_mod(int a, int b)

{
	return (a % b);
}
